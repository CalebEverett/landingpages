  <h1>8 Month Medical Assistant Career Training Program in Santa Ana, California</h1>
  <p>From small medical offices to large hospitals, the demand for healthcare workers is projected to continue to grow well into the future. Health care industry expansion, technological advances and the growing elderly population are all factors that make the healthcare industry a great career choice for job security and personal fulfillment.</p>
  <p>If you’re a strong multitasker who enjoys interacting with and helping others, and you’re looking to start a rewarding career (<strong>without spending years in school</strong>), you owe it to yourself to take two minutes to explore your opportunities as a trained Medical Assistant. </p>
  <h2>Becoming A Qualified Medical Assistant</h2>
  <p>The Medical Assistant Program from Career College of California (CCC) provides the <strong>hands-on training</strong>, practical experience and industry support it takes to pursue a professional healthcare career.</p>
  <p>Students in this program obtain both the <strong>administrative and clinical skills</strong> required to perform both front- and back-office duties in many health care institutions. This practical, career-focused program is led by knowledgeable medical professionals with years of experience working in the field. You’ll learn:</p>
  <div class="columns">
    <ul>
      <li>Medical Terminology</li>
      <li>Computer Basics</li>
      <li>Charting and Documentation</li>
      <li>How to Conduct Physical Examinations &amp; Take Vital Signs</li>
      <li>Medical Billing and Collections</li>
      <li>American Heart Association-Certified CPR</li>
      <li>Legal and Ethical Considerations and HIPAA policies</li>
      <li>Basic Pharmacology</li>
      <li>Medication Dosage and Calculations</li>
    </ul>
  </div>
  <p>As a student you’ll get the up-to-date skills it takes to make a smooth transition into any modern healthcare setting.</p>
