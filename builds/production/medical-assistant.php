  <h2>Real World Experience</h2>
  <p>Healthcare is about helping people, and we believe the best way to learn Medical Assisting is <strong>on-the-job</strong>, working with real patients and other medical professionals. As a student in this program you’ll have the opportunity to complete a <strong>professional externship in a real healthcare setting</strong>. <br><br>You’ll have the chance to gain practical on-the-job experience working with experienced medical professionals and make important industry connections before you graduate.</p>
  <h2>Get Certified</h2>
  <p>As a graduate of this program you’ll be fully prepared and eligible to become certified by the <strong>National Healthcareer Association (NHA)</strong>. This certification shows employers that you have the skills they’re looking for and demonstrates that you’ll be an asset to any workplace. </p>
  <h2>Convenient Day &amp; Evening Schedules</h2>
  <p>At CCC, we know the time pressures our students face. Many students are working and/or raising families while going to school. Our programs are specifically designed to <strong>fit the needs of busy, adult learners</strong>. As a Medical Assistant student, you can choose from either the day or evening schedule - whichever works best for you!</p>
